/* ═══════════════════════════════════════════
   MOTEUR MULTILINGUE KOTCHAMI (i18n)
   - 6 langues, RTL pour l'arabe
   - mémorise le choix (localStorage)
   - écran de choix au 1er passage
   - bascule via [data-i18n]
═══════════════════════════════════════════ */
window.KLANGS = [
  {code:'fr', name:'Français',  tag:'FR',    flag:'🇫🇷', rtl:false},
  {code:'en', name:'English',   tag:'EN',    flag:'🇬🇧', rtl:false},
  {code:'ar', name:'العربية',   tag:'AR',    flag:'🇸🇦', rtl:true },
  {code:'es', name:'Español',   tag:'ES',    flag:'🇪🇸', rtl:false},
  {code:'pt', name:'Português', tag:'PT',    flag:'🇵🇹', rtl:false},
  {code:'sw', name:'Kiswahili', tag:'SW',    flag:'🇰🇪', rtl:false}
];

window.applyLang = function(code){
  var lang = window.KLANGS.find(function(l){return l.code===code;}) || window.KLANGS[0];
  var dict = (window.KI18N && window.KI18N[code]) ? window.KI18N[code] : {};
  var fallback = (window.KI18N && window.KI18N['fr']) ? window.KI18N['fr'] : {};
  // texte
  document.querySelectorAll('[data-i18n]').forEach(function(el){
    var key = el.getAttribute('data-i18n');
    var val = dict[key] || fallback[key];
    if(val!=null){
      // conserve un éventuel <br> via le marqueur |
      el.innerHTML = val.split('||').join('<br>');
    }
  });
  // placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el){
    var key=el.getAttribute('data-i18n-ph');var val=dict[key]||fallback[key];
    if(val!=null) el.setAttribute('placeholder',val);
  });
  // direction RTL/LTR
  document.documentElement.setAttribute('lang',code);
  document.documentElement.setAttribute('dir', lang.rtl ? 'rtl' : 'ltr');
  // mémoriser
  try{ localStorage.setItem('kotchami_lang', code); }catch(e){}
  // mettre à jour l'indicateur du header s'il existe
  var ind = document.getElementById('langCurrent');
  if(ind) ind.innerHTML = '<span style="font-size:0.95rem">🌐</span> ' + lang.tag;
  var menuEl = document.getElementById('langMenu');
  if(menuEl) Array.prototype.forEach.call(menuEl.children, function(b){
    var on = (b.getAttribute('data-code')===code);
    b.style.background = on ? 'rgba(197,158,80,0.16)' : 'none';
    b.style.color = on ? '#F5D98B' : '#F4F7FF';
  });
};

window.getLang = function(){
  try{ return localStorage.getItem('kotchami_lang'); }catch(e){ return null; }
};

/* écran de choix au 1er passage */
window.initLangChooser = function(){
  var saved = window.getLang();
  if(saved){ window.applyLang(saved); return; }
  // pas de choix -> afficher l'écran
  var ov = document.getElementById('langOverlay');
  if(ov){ ov.classList.add('show'); document.body.style.overflow='hidden'; }
  else { window.applyLang('fr'); }
};

window.chooseLang = function(code){
  window.applyLang(code);
  var ov = document.getElementById('langOverlay');
  if(ov){ ov.classList.remove('show'); document.body.style.overflow=''; }
};

/* ════════ AUTO-INIT (commun à toutes les pages) ════════ */
(function(){
  function buildUI(){
    var list=document.getElementById('langList');
    if(list && !list.dataset.built){
      list.dataset.built='1';
      window.KLANGS.forEach(function(l){
        var b=document.createElement('button');
        b.className='lang-item';
        b.onclick=function(){window.chooseLang(l.code);};
        b.innerHTML='<span class="lang-badge" style="display:inline-flex;align-items:center;justify-content:center;width:44px;height:44px;border-radius:13px;background:rgba(197,158,80,0.16);color:#F5D98B;font-family:\'JetBrains Mono\',monospace;font-weight:700;font-size:1rem;flex-shrink:0;letter-spacing:.04em;">'+l.tag+'</span><span class="lang-info"><span class="lang-name">'+l.name+'</span></span><span class="lang-radio"></span>';
        list.appendChild(b);
      });
    }
    var menu=document.getElementById('langMenu');
    if(menu && !menu.dataset.built){
      menu.dataset.built='1';
      window.KLANGS.forEach(function(l){
        var b=document.createElement('button');
        b.setAttribute('data-code', l.code);
        b.style.cssText='display:flex;align-items:center;justify-content:space-between;gap:16px;width:100%;background:none;border:none;color:#F4F7FF;padding:10px 12px;border-radius:8px;cursor:pointer;font-family:\'Inter\',sans-serif;font-size:0.88rem;text-align:left;transition:background .15s;';
        b.innerHTML='<span>'+l.name+'</span><span style="opacity:.5;font-size:.7rem;font-family:\'JetBrains Mono\',monospace;letter-spacing:.05em;">'+l.tag+'</span>';
        b.onmouseover=function(){ b.style.background='rgba(255,255,255,0.06)'; };
        b.onmouseout=function(){ b.style.background=(l.code===window.getLang())?'rgba(197,158,80,0.16)':'none'; };
        b.onclick=function(){window.applyLang(l.code);menu.classList.remove('open');};
        menu.appendChild(b);
      });
    }
    document.addEventListener('click',function(e){
      var sw=document.querySelector('.lang-switch');
      if(sw && menu && !sw.contains(e.target)) menu.classList.remove('open');
    });
    /* ── accessibilité (WCAG) du sélecteur ── */
    var cur=document.getElementById('langCurrent');
    if(cur && menu){
      menu.setAttribute('role','menu');
      menu.setAttribute('aria-label','Langues / Languages');
      Array.prototype.forEach.call(menu.children,function(b){b.setAttribute('role','menuitem');});
      cur.setAttribute('aria-haspopup','menu');
      cur.setAttribute('aria-controls','langMenu');
      cur.setAttribute('aria-label','Changer de langue / Change language');
      var sync=function(){ cur.setAttribute('aria-expanded', menu.classList.contains('open')?'true':'false'); };
      sync();
      cur.addEventListener('click',function(){ setTimeout(sync,0); });
      document.addEventListener('keydown',function(e){
        if(e.key==='Escape' && menu.classList.contains('open')){ menu.classList.remove('open'); sync(); cur.focus(); }
      });
      menu.addEventListener('click',function(){ setTimeout(sync,0); });
    }
    window.initLangChooser();
  }
  if(document.readyState==='loading') document.addEventListener('DOMContentLoaded',buildUI);
  else buildUI();
})();
